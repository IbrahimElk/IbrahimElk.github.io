function switchTab(btn, targetId) {
  const panel = btn.closest('.interactive-panel') ||
                btn.closest('.panel-body') ||
                btn.closest('section');

  const allBtns = btn.parentElement.querySelectorAll('.tab-btn');
  allBtns.forEach(b => b.classList.remove('active'));
  btn.classList.add('active');

  let container = btn.closest('.panel-body');
  if (!container) container = btn.parentElement.parentElement;
  container.querySelectorAll('.tab-pane').forEach(p => p.classList.remove('active'));
  const target = document.getElementById(targetId);
  if (target) target.classList.add('active');
}

function toggleCollapse(trigger) {
  trigger.classList.toggle('open');
  const body = trigger.nextElementSibling;
  body.classList.toggle('open');
}

let qkvFused = false;
function toggleQKV() {
  qkvFused = !qkvFused;
  document.getElementById('qkv-before').classList.toggle('active', !qkvFused);
  document.getElementById('qkv-after').classList.toggle('active', qkvFused);
  document.getElementById('qkv-status').innerHTML = 
    qkvFused ? 'Showing: <strong style="color:var(--green)">After fusion ✓</strong>'
             : 'Showing: <strong>Before fusion</strong>';
}

